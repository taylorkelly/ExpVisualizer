#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    if(indexPath.section == 0) {
        DetailedPrivacySettings *details = [[DetailedPrivacySettings alloc] initWithUser:@"Default Settings"];
        [self.navigationController pushViewController:details animated:YES];
        [details release];        
    } else {
        DetailedPrivacySettings *details = [[DetailedPrivacySettings alloc] initWithUser:[self.peopleArray objectAtIndex:indexPath.row]];
        [self.navigationController pushViewController:details animated:YES];
        [details release];
    }
}

