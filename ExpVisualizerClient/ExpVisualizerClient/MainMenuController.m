#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    if(indexPath.row == 0) {
        PrivacySettings *settings = [[PrivacySettings alloc] init];
        [self.navigationController pushViewController:settings animated:YES];
        [settings release];
    } else if(indexPath.row == 1) {
        ImageShareController *imageShare = [[ImageShareController alloc] init];
        [self.navigationController pushViewController:imageShare animated:YES];
        [imageShare release];
    } else if(indexPath.row == 2) {
        ViewController *testing = [[ViewController alloc] init];
        [self.navigationController pushViewController:testing animated:YES];
        [testing release];
    }
}

